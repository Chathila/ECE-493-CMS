CREATE TABLE IF NOT EXISTS user_accounts (
    user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name VARCHAR(200),
    email VARCHAR(320) NOT NULL UNIQUE,
    password_hash VARCHAR(512) NOT NULL,
    password_salt VARCHAR(512) NOT NULL,
    status VARCHAR(20) NOT NULL,
    role VARCHAR(30) NOT NULL DEFAULT 'AUTHOR',
    created_at TIMESTAMP NOT NULL
);

CREATE TABLE IF NOT EXISTS paper_submissions (
    submission_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    author_email VARCHAR(320) NOT NULL,
    title VARCHAR(500) NOT NULL,
    authors CLOB NOT NULL,
    affiliations CLOB NOT NULL,
    abstract_text CLOB NOT NULL,
    keywords CLOB NOT NULL,
    contact_details VARCHAR(500) NOT NULL,
    manuscript_file_id BIGINT NOT NULL,
    submitted_at TIMESTAMP NOT NULL
);

CREATE TABLE IF NOT EXISTS paper_submission_drafts (
    draft_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    author_email VARCHAR(320) NOT NULL UNIQUE,
    title VARCHAR(500) NOT NULL,
    authors CLOB,
    affiliations CLOB,
    abstract_text CLOB,
    keywords CLOB,
    contact_details VARCHAR(500) NOT NULL,
    updated_at TIMESTAMP NOT NULL
);
;
